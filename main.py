def print_world(world,result):
    """Display the map"""
    # pylint: disable=consider-using-enumerate
    for y in reversed(range(len(world[0]))):
        for x in range(len(world)):
            if world[x][y] == 0:
                print("██",end="")
            elif (x,y) in result["path"]:
                print("[]",end="")
            elif (x,y) in result["cloud"]:
                print("..",end="")
            elif (x,y) in result["rim"]:
                print("::",end="")
            else:
                print("  ",end="")
        print()

